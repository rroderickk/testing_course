/**
 * `sum` is a function that takes two numbers, `a` and `b`,
 * and returns the sum of those two numbers
 *
 * @param a The first number to add
 * @param b The second number to add
 */
export const sum  = (a, b) => a + b

/**
 * `subt` takes two numbers and returns the difference between them
 *
 * @param a The first number to subtract from.
 * @param b The second number to subtract from the first.
 */
export const subt = (a, b) => a - b

/**
 * Mult multiplies two numbers.
 *
 * @param a The first number to multiply
 * @param b The second parameter
 */
export const mult = (a, b) => a * b

/**
 * Divide a by b
 *
 * @param a The first number
 * @param b The second number to divide by
 */
export const div  = (a, b) => a / b

/**
 * Mod returns the remainder of a divided by b.
 *
 * @param a The first number
 * @param b The base number
 */
export const mod  = (a, b) => a % b